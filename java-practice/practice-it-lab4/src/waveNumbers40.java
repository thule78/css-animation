public class waveNumbers40 {
    public static void main(String[] args){
        // dash line
        for(int i = 1; i <= 40; i++){
            System.out.print("-");
        }
        //break line
        System.out.println();
        //each have 4 characters _-^- and total 40 character perline 40 divide 4 = 10
        for(int j = 1; j <= 10; j++){
            System.out.print("_-^-");
        }
        //break line
        System.out.println();
        //number as we have 2 set of numbers 11223344556677889900
        for(int z = 1; z <= 2; z++){
            System.out.print("11223344556677889900");
        }
        //break line
        System.out.println();
        //dash line
        for(int x = 1; x <= 40; x++){
            System.out.print("_");
        }
    }
}
